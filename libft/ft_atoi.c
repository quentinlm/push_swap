#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = 1;
	b = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			a *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		b = b * 10 + str[i] - '0';
		i++;
	}
	return (b * a);
}
